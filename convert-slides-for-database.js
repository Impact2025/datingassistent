// Converteer naar DatabaseSlidesData format
function convertToDatabaseFormat(slidesData) {
  const convertedSlides = slidesData.slides.map((slide, index) => {
    let content = '';

    // Build content based on slide type
    switch (slide.type) {
      case 'title':
        content = `<h1 style="font-size: 2.5rem; font-weight: bold; margin-bottom: 1rem;">${slide.title}</h1>`;
        if (slide.subtitle) {
          content += `<h2 style="font-size: 1.5rem; font-weight: normal; color: #666; margin-bottom: 2rem;">${slide.subtitle}</h2>`;
        }
        break;

      case 'quote':
        content = `<blockquote style="font-size: 1.5rem; font-style: italic; text-align: center; padding: 2rem; border-left: 4px solid #E14874; background: #f8f9fa; margin: 2rem 0;">
          <p>"${slide.quote}"</p>
          ${slide.author ? `<cite style="display: block; margin-top: 1rem; font-size: 1rem; color: #666;">â€” ${slide.author}</cite>` : ''}
        </blockquote>`;
        break;

      case 'content':
        content = `<div style="max-width: none;">`;
        if (slide.title) {
          content += `<h3 style="font-size: 1.5rem; font-weight: bold; margin-bottom: 1rem;">${slide.emoji ? slide.emoji + ' ' : ''}${slide.title}</h3>`;
        }
        if (slide.content) {
          content += `<p style="margin-bottom: 1.5rem; line-height: 1.6;">${slide.content}</p>`;
        }
        if (slide.bullets && slide.bullets.length > 0) {
          content += `<ul style="margin: 1rem 0; padding-left: 1.5rem;">`;
          slide.bullets.forEach(bullet => {
            content += `<li style="margin-bottom: 0.5rem; line-height: 1.6;">${bullet}</li>`;
          });
          content += `</ul>`;
        }
        content += `</div>`;
        break;

      case 'split':
        content = `<div style="display: grid; grid-template-columns: 1fr 1fr; gap: 2rem; margin: 2rem 0;">
          <div style="padding: 1.5rem; background: #f8f9fa; border-radius: 8px; border-left: 4px solid #dc3545;">
            <h4 style="font-weight: bold; margin-bottom: 1rem; color: #dc3545;">${slide.title}</h4>
            <div style="white-space: pre-line; line-height: 1.6;">${slide.leftContent}</div>
          </div>
          <div style="padding: 1.5rem; background: #f8f9fa; border-radius: 8px; border-left: 4px solid #28a745;">
            <h4 style="font-weight: bold; margin-bottom: 1rem; color: #28a745;">Oplossing</h4>
            <div style="white-space: pre-line; line-height: 1.6;">${slide.rightContent}</div>
          </div>
        </div>`;
        break;

      case 'checklist':
        content = `<div style="background: #f8f9fa; padding: 2rem; border-radius: 8px; margin: 2rem 0;">
          <h3 style="font-size: 1.25rem; font-weight: bold; margin-bottom: 1rem;">${slide.emoji ? slide.emoji + ' ' : ''}${slide.title}</h3>
          <ul style="list-style: none; padding: 0;">`;
        slide.items.forEach(item => {
          const checked = item.checked ? 'âœ…' : 'â¬œ';
          content += `<li style="margin-bottom: 0.75rem; display: flex; align-items: center; gap: 0.75rem;">
            <span style="font-size: 1.25rem;">${checked}</span>
            <span>${item.text}</span>
          </li>`;
        });
        content += `</ul></div>`;
        break;

      default:
        content = `<p>Onbekend slide type: ${slide.type}</p>`;
    }

    return {
      id: `slide-${index + 1}`,
      title: slide.title || `Slide ${index + 1}`,
      content: content,
      notes: slide.type === 'content' && slide.bullets ?
        `Tip: Neem de tijd om elke bullet point te overdenken en toe te passen.` : undefined
    };
  });

  return {
    title: slidesData.title,
    description: 'Leer de basis van zelfkennis en authenticiteit voor betere dating profielen',
    slides: convertedSlides
  };
}

